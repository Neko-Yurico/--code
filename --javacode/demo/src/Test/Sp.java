public class Sp {
    public String Port ;
    public static SerialPort sp;
    public static boolean isStream = false;//判断流水灯是否开启
    
    public static void open ( ){
        isStream=true;
    }
    
    public static void close ( ){
        isStream=false;
    }
    
    public static void change ( ) {
        isStream = ! isStream;
    }
    public void setPort(String s){
        Port=s;
    }
    public Sp(String Port) {
        sp = SerialPort.getCommPort ( Port );
        sp.setBaudRate ( 9600 );
        if ( ! sp.isOpen ( ) ) {
            sp.openPort ( );
        }
    }
    public static void ControlLed ( byte[] bytes ){
        sp.writeBytes ( bytes , 1 );
    }
}
